       public void exibirListaClientes(){
       DefaultTableModel tm = (DefaultTableModel) tblListaClientes.getModel();
       tm.setNumRows(0);
       
       BCliente bcliente = new BCliente();
       
       ArrayList<Cliente> listaCliente = bcliente.lista();
       for (Cliente cliente:listaCliente){
           Object[] linha = {cliente.getNumero(), cliente.getNome()};
           tm.addRow(linha);
       }
    }
