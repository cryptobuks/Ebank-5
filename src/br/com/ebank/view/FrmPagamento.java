    private void btnPagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagarActionPerformed
        // TODO add your handling code here:
        if (this.isPaga()){
        JOptionPane.showMessageDialog(null, "Esta fatura j√° foi paga.");
        this.dispose();
        }else{
        BConta bconta = new BConta();
        BPagamento bpagamento = new BPagamento();
        double valor = bpagamento.buscar(nboleto).getValor();
            try {
                if(bconta.sacar(valor, nconta, bconta.pegaSaldo(nconta))){
                    bpagamento.pagar(nboleto);
                    JOptionPane.showMessageDialog(null,String.format("Conta paga com sucesso, retire o comprovate no local indicado."));
                    this.dispose();
                    bconta.gravaPag(valor, this.nconta, bpagamento.buscar(nboleto).getNumero());
                }
                else {
                   JOptionPane.showMessageDialog(null,String.format("Saldo Insuficiente")); 
                }
            } catch (SQLException ex) {
                Logger.getLogger(FrmPagamento.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
      
    }//GEN-LAST:event_btnPagarActionPerformed

